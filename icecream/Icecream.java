package house.persnal.re.icecream;

public class Icecream {
	private String name;
	private int price;
	
	Icecream(){}
	Icecream(String name, int price){
		this.name = name;
		this.price = price;
		}
	public String getName() {
		return name;
	}
	public int getPrice() {
		return price;
	}
	
}
